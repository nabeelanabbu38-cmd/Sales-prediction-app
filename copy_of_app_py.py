import streamlit as st
import pickle
import numpy as np

# Load trained model
model = pickle.load(open("model.pkl", "rb"))

st.title("Sales Prediction Web App")
st.write("Enter details to predict future sales:")

price = st.number_input("Price", 50, 500)
advertising = st.number_input("Advertising Budget", 0, 10000)
promo = st.selectbox("Promotion", [0, 1])

if st.button("Predict Sales"):
    prediction = model.predict([[price, advertising, promo]])[0]
    st.success(f"Predicted Sales: {round(prediction, 2)}")